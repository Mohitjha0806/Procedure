CREATE PROCEDURE dbo.employeeData
@EMAIL VARCHAR(50) = NULL,
@PASSWORD VARCHAR(50) = NULL,
@FIRST_NAME VARCHAR(50) = NULL,
@LAST_NAME VARCHAR(50) = NULL,
@NUMBER VARCHAR(50) = NULL

AS
BEGIN

SET NOCOUNT ON

INSERT INTO employee
(
EMAIL,
PASSWORD,
FIRST_NAME,
LAST_NAME,
NUMBER
)
VALUES
(
@EMAIL,
@PASSWORD,
@FIRST_NAME,
@LAST_NAME,
@NUMBER
)
END;

GO